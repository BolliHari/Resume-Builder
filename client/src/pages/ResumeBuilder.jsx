import React, { useEffect, useState } from 'react'
import { Link, useParams } from 'react-router-dom'
import { dummyResumeData } from '../assets/assets'
import { ArrowLeftIcon, Briefcase, ChevronLeft, ChevronRight, Download, DownloadIcon, EyeIcon, EyeOff, FileText, FolderIcon, GraduationCap, Share2Icon, SparkleIcon, Sparkles, User2 } from 'lucide-react'
import PersonalInfoForm from '../components/PersonalInfoForm'
import ResumePreview from '../components/ResumePreview'
import TemplateSelector from '../components/TemplateSelector'
import ColorPicker from '../components/ColorPicker'
import ProfessionalSummaryForm from '../components/ProfessionalSummaryForm'
import ExperienceForm from '../components/ExperienceForm'
import EducationForm from '../components/home/EducationForm'
import ProjectForm from '../components/home/ProjectForm'
import SkillForm from '../components/SkillForm'
import api from '../configs/api'
import { useSelector } from 'react-redux'
import toast from 'react-hot-toast'


const ResumeBuilder = () => {

  const { resumeId } = useParams()

  const { token} = useSelector(state => state.auth)

  const [resumeData, setResumeData] = useState({
    _id: '',
    title: '',
    personal_info: {},
    experience: [],
    education: [],
    project: [],
    skills: [],
    templates: "classic",
    accent_color: "#3B82F6",
    public: false,
  })

  const loadExistingResume = async () => {
    try {
      const { data } = await api.get('/api/resume/get/'+resumeId , {headers : {
        Authorization : token
      }})
      if (data.resume){
        setResumeData(data.resume)
      }
    } catch (error) {
      console.log(error.message)
    }
  }

  const [activeSectionIndex, setActiveSectionIndex] = useState(0)
  const [removeBackground, setRemoveBackground] = useState(false)

  const sections = [
    { id: 'personal', name: 'Personal Info',icon: User2 },
    { id: 'summary', name: 'Summary',icon: FileText },
    { id: 'experience', name: 'Experience',icon: Briefcase },
    { id: 'education', name: 'Education',icon: GraduationCap },
    { id: 'projects', name: 'Projects',icon: FolderIcon },
    { id: 'skills', name: 'Skills',icon: Sparkles },
  ]

  const activeSection = sections[activeSectionIndex]

  useEffect(() => {
    loadExistingResume()
  },[])

  const changeresumeVisibility = async () => {
    try {
      const formData = new FormData()
      formData.append("resumeId", resumeId)
      formData.append("resumeData",JSON.stringify({public:!resumeData.public}))

      const { data } = await api.put('/api/resume/update', formData, {headers:{
        Authorization : token
      }})

      setResumeData({...resumeData, public: !resumeData.public})
      toast.success(data.message)

    } catch (error) {
      console.log(error.message)
    }
  }

  const handleshare = () => {
    const frontendUrl = window.location.href.split('/app')[0];
    const resumeUrl = frontendUrl + '/view/' + resumeId;

    if (navigator.share) {
      navigator.share({url: resumeUrl, text: 'My Resume'})
    }
    else{
      alert('Your browser does not support sharing. Please copy the link: ' + resumeUrl)
    }
  }

  const downloadResume = () => {
    window.print();
  }


  const saveResume = async () => {
    try {
      let updatedResumeData = structuredClone(resumeData)

      if (typeof resumeData.personal_info.image === 'object') {
        delete updatedResumeData.personal_info.image
      }

      const formData = new FormData();
      formData.append("resumeId",resumeId)
      formData.append("resumeData",JSON.stringify(updatedResumeData))
      removeBackground && formData.append("removeBackground","yes")
      typeof resumeData.personal_info.image === 'object' && formData.append("image",resumeData.personal_info.image)

      const { data } = await api.put('/api/resume/update', formData, {headers:{
        Authorization : token
      }})
      

      setResumeData(data.resume)
      toast.success(data.message)
    } catch (error) {
      console.log("error from saveResume",error)
    }
  }

  return (
    <div>
      <div className="px-4 py-6 mx-auto max-w-7xl">
        <Link to={'/app'} className="inline-flex gap-2 items-center text-slate-500 hover:text-slate-700 transition-all">
          <ArrowLeftIcon className="size-4" /> Back to Dashboard
        </Link>
      </div>
      <div className="max-w-7xl mx-auto px-4 pb-8">
        <div className="grid grid-cols-1 lg:grid-cols-12 gap-8">
          {/* {Left panel} */}
          <div className="relative lg:col-span-5 rounded-lg overflow-hidden">
            <div className="bg-white border border-slate-200 shadow-sm rounded-lg p-6 pt-1">
              {/* progress bar */}
              <hr className='absolute top-0 left-0 right-0 border-2 border-slate-200' />
              <hr className='absolute top-0 left-0 h-1 border-none bg-gradient-to-r from-green-500 to-green-600 transition-all duration-2000' 
              style={{width: `${activeSectionIndex * 100 / (sections.length - 1)}%`}}/>

              {/* navigation section */}
              <div className="flex items-center justify-between mb-8 border-b py-1 border-slate-200">
                <div className="flex items-center gap-4">
                  <TemplateSelector selectedTemplate={resumeData.templates} onChange={(template) => setResumeData(prev => ({...prev, templates: template}))} />
                  <ColorPicker selectedColor={resumeData.accent_color} onChange={(color) => setResumeData(prev => ({...prev, accent_color: color}))} />
                </div>
                <div className="flex items-center">
                  {activeSectionIndex !== 0 && (
                      <button onClick={() => setActiveSectionIndex(prevind => Math.max(prevind-1,0))} className='flex items-center gap-1 p-3 rounded-lg hover:bg-gray-50 transition-all text-sm font-medium text-slate-600'
                      disabled={activeSectionIndex === 0}>
                        <ChevronLeft className='size-4'/> Previous
                      </button>
                  )
                  }
                  <button onClick={() => setActiveSectionIndex(prevind => Math.min(prevind+1,sections.length - 1))} className={`flex items-center gap-1 p-3 rounded-lg hover:bg-gray-50 transition-all text-sm font-medium text-slate-600 ${activeSectionIndex === sections.length - 1 && 'opacity-50 cursor-not-allowed'}`}
                      disabled={activeSectionIndex === sections.length - 1}>
                        Next <ChevronRight className='size-4'/>
                      </button>
                </div>
              </div>
              {/* active section form */}
              <div className="space-y-6">
                  {
                    activeSection.id === 'personal' && (
                      <PersonalInfoForm data={resumeData.personal_info} removeBackground={removeBackground} setRemoveBackground={setRemoveBackground} onChange={(data) => setResumeData(prev => ({...prev, personal_info: data}))} /> 
                    )
                  }
                  {
                    activeSection.id === 'summary' && (
                      <ProfessionalSummaryForm data={resumeData.professional_summary} onChange={(data) => setResumeData(prev => ({...prev, professional_summary: data}))} setResumeData={setResumeData} />
                    )
                  }
                  {
                    activeSection.id === 'experience' && (
                      <ExperienceForm data={resumeData.experience} onChange={(data) => setResumeData(prev => ({...prev, experience: data}))} />
                    )
                  }
                  {
                    activeSection.id === 'education' && (
                      <EducationForm data={resumeData.education} onChange={(data) => setResumeData(prev => ({...prev, education: data}))} />
                    )
                  }
                  {
                    activeSection.id === 'projects' && (
                      <ProjectForm data={resumeData.project} onChange={(data) => setResumeData(prev => ({...prev, project: data}))} />
                    )
                  }
                  {
                    activeSection.id === 'skills' && (
                      <SkillForm data={resumeData.skills} onChange={(data) => setResumeData(prev => ({...prev, skills: data}))} />
                    )
                  }
              </div>
              <button onClick={() => {toast.promise(saveResume,{loading:'saving...'})}} className='bg-gradient-to-br from-green-100 to-green-200 px-6 py-2 rounded-md mt-6 text-sm text-green-600 ring-green-300 ring hover:ring-green-500 transition-all'>
                Save Changes
              </button>
            </div>
          </div>
          {/* right panel */}
          <div className="lg:col-span-7 mt-6 lg:mt-0">
            <div className="relative w-full">
              <div className="absolute bottom-3 left-0 right-0 flex items-center justify-end gap-2">
                  {resumeData.public && (
                    <button onClick={handleshare} className='flex items-center gap-2 p-2 px-4 bg-gradient-to-br from-blue-100 to-blue-200 text-xs text-blue-600 rounded-lg ring-blue-300 hover:ring transition-colors'>
                      <Share2Icon className='size-4'/>
                      Share
                    </button>
                  )}
                  <button onClick={changeresumeVisibility} className='flex items-center gap-2 p-2 px-4 bg-gradient-to-br from-green-100 to-green-200 text-xs text-green-600 rounded-lg ring-green-300 hover:ring-green-500 transition-colors'>
                    {resumeData.public ? <EyeIcon className='size-4'/> : <EyeOff className='size-4' />}
                    {resumeData.public ? 'Public' : 'Private'}
                  </button>
                  <button onClick={downloadResume} className='flex items-center gap-2 p-2 px-4 bg-gradient-to-br from-purple-100 to-purple-200 text-xs text-purple-600 rounded-lg ring-purple-300 hover:ring-purple-500 transition-colors'>
                    <DownloadIcon className='size-4'/>
                    Download
                  </button>
              </div>
            </div>
            <ResumePreview data={resumeData} template={resumeData.templates} accentColor={resumeData.accent_color} />
            
          </div>
        </div>
      </div>
    </div>
  )
}

export default ResumeBuilder
